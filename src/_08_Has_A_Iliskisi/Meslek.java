package _08_Has_A_Iliskisi;

public class Meslek {

    private int id;
    private String adi;
    private long ucret;

    private int gunSayisi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAdi() {
        return adi;
    }

    public void setAdi(String adi) {
        this.adi = adi;
    }

    public long getUcret() {
        return ucret;
    }

    public void setUcret(long ucret) {
        this.ucret = ucret;
    }

    public int getGunSayisi() {
        return gunSayisi;
    }

    public void setGunSayisi(int gunSayisi) {
        this.gunSayisi = gunSayisi;
    }
}
